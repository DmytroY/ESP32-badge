ST7789_76x284::ST7789_76x284(uint16_t w, uint16_t h, uint16_t x_off, uint16_t y_off)
    : _w(w), _h(h), _x_off(x_off), _y_off(y_off) {}

void ST7789_76x284::begin(int8_t sclk, int8_t mosi, int8_t cs, int8_t dc, int8_t rst){
    _cs = cs;
    _dc = dc;
    _rst = rst;
    pinMode(_rst, OUTPUT);
    pinMode(_cs,  OUTPUT);
    pinMode(_dc,  OUTPUT);
    digitalWrite(_cs, HIGH);

    SPI.begin(sclk, -1, mosi, _cs);
    SPI.setFrequency(27000000);
    SPI.setDataMode(SPI_MODE0);
    SPI.setBitOrder(MSBFIRST);

    //TFT init procedure
    digitalWrite(_rst, HIGH); delay(10);
    digitalWrite(_rst, LOW);  delay(50);
    digitalWrite(_rst, HIGH); delay(150);

    send_cmd(0x01); delay(150);  // Software reset
    send_cmd(0x11); delay(255);  // Sleep out
    
    uint8_t colmod[] = { 0x55 };           // 16-bit color
    send_cmd_data(0x3A, colmod, 1); delay(10);

    uint8_t madctl;
    if(_w > _h){
        madctl = { 0x68 };           // MX + MV + BGR (landscape)
    } else {
        madctl = { 0x08 };           // BGR, (portrait)
    }
    send_cmd_data(0x36, &madctl, 1);

    send_cmd(0x20); delay(10);   // invertion OFF
    send_cmd(0x13); delay(10);   // Partial mode OFF
    send_cmd(0x29); delay(255);  // Display ON
}

void ST7789_76x284::write_byte(uint8_t b){SPI.transfer(b);}

void ST7789_76x284::send_cmd(uint8_t cmd){
    digitalWrite(_dc, LOW);
    digitalWrite(_cs, LOW);
    write_byte(cmd);
    digitalWrite(_cs, HIGH);
}

void ST7789_76x284::send_data(uint8_t data){
    digitalWrite(_dc, HIGH);
    digitalWrite(_cs, LOW);
    write_byte(data);
    digitalWrite(_cs, HIGH);
}

void ST7789_76x284::send_cmd_data(uint8_t cmd, const uint8_t* data, uint8_t len){
    digitalWrite(_dc, LOW);
    digitalWrite(_cs, LOW);
    write_byte(cmd);
    digitalWrite(_dc, HIGH);
    for(uint8_t i = 0; i < len; i++) write_byte(data[i]);
    digitalWrite(_cs, HIGH);
}

void ST7789_76x284::set_window(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1){
    x0 += _x_off;
    x1 += _x_off;
    y0 += _y_off;
    y1 += _y_off;
    uint8_t cx[] = { (uint8_t)(x0 >> 8), (uint8_t)(x0), (uint8_t)(x1 >> 8), (uint8_t)(x1) };
    uint8_t cy[] = { (uint8_t)(y0 >> 8), (uint8_t)(y0), (uint8_t)(y1 >> 8), (uint8_t)(y1) };
    send_cmd_data(0x2A, cx, 4); // set column address
    send_cmd_data(0x2B, cy, 4); // set row address
    send_cmd(0x2C);  // Memory write
}

void ST7789_76x284::fillRect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color){
    set_window(x, y, x + w - 1, y + h - 1);
    digitalWrite(_dc, HIGH);
    digitalWrite(_cs, LOW);
    for (uint32_t i = 0; i < (uint32_t)w * h; i++) {
        write_byte(color >> 8); write_byte(color & 0xFF);
    }
    digitalWrite(_cs, HIGH);
}

void ST7789_76x284::fillScreen(int16_t color){fillRect(0, 0, _w, _h, color);}

void ST7789_76x284::drawPixel(int16_t x, int16_t y, uint16_t color){
    set_window(x, y, x, y);
    digitalWrite(_dc, HIGH);
    digitalWrite(_cs, LOW);
    write_byte(color >> 8);
    write_byte(color & 0xFF);
    digitalWrite(_cs, HIGH);
}

void ST7789_76x284::drawHLine(int16_t x, int16_t y, int16_t w, uint16_t color) {fillRect(x, y, w, 1, color);}

void ST7789_76x284::drawVLine(int16_t x, int16_t y, int16_t h, uint16_t color) {fillRect(x, y, 1, h, color);}

void ST7789_76x284::drawRect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color){
    drawHLine(x,         y,         w, color);
    drawHLine(x,         y + h - 1, w, color);
    drawVLine(x,         y,         h, color);
    drawVLine(x + w - 1, y,         h, color);
}

