void ST7789_76x284::drawChar(int16_t x, int16_t y, char c, uint16_t fg, uint16_t bg, uint8_t scale) {
    if (c < ' ' || c > '~') return;
    uint8_t idx = c - ' ';

    if (scale % 2) {
        // based on 1 byte per column font_5x7 scaled up.
        const uint8_t* g = font_5x7[idx];
        for (uint8_t col = 0; col < 5; col++) {
            uint8_t v_line = pgm_read_byte(&g[col]);
            for (uint8_t row = 0; row < 7; row++){
                uint16_t color = (v_line & (1 << row)) ? fg : bg;
                fillRect(x + col * scale, y + row * scale, scale, scale, color);
            }
        }
    } else {
        // based on 2 bytes per column font_10x14 scaled up
        const uint16_t* g = font_10x14[idx];
        for(uint8_t col = 0; col < 10; col++){
            uint16_t v_line = pgm_read_word(&g[col]);
            for (uint8_t row = 0; row < 14; row++){
                uint16_t color = (v_line & (1 << row)) ? fg : bg;
                fillRect(x + col * scale/2, y + row * scale/2, scale/2, scale/2, color);
            }
        }
    }
    
    // Character gap
    fillRect(x + 5 * scale, y, scale, 7 * scale, bg);
}

void ST7789_76x284::drawText(int16_t x, int16_t y, const char* str, uint16_t fg, uint16_t bg, uint8_t scale){
    int16_t cx = x;
    while (*str) {
        if (*str == '\n') {
            cx = x;
            y += 8 * scale;
        } else {
            drawChar(cx, y, *str, fg, bg, scale);
            cx += 6 * scale;
        }
        str++;
    }
}

void ST7789_76x284::drawQR(const char* str, int16_t x, int16_t y) {

    const uint8_t QR_VERSION  = 3;
    const uint8_t QUIET       = 2;
    const uint16_t ZONE       = 76;

    // ── Generate QR ──────────────────────────────────────────────────
    uint8_t* buf = (uint8_t*)malloc(qrcode_getBufferSize(QR_VERSION));
    if (!buf) return;
    QRCode qr;
    qrcode_initText(&qr, buf, QR_VERSION, ECC_LOW, str);

    // ── Block size and centering ──────────────────────────────────────
    uint8_t  block    = ZONE / (qr.size + 2 * QUIET);
    uint16_t total_px = (qr.size + 2 * QUIET) * block;
    uint16_t offset_x = (x < 0 ? _w - ZONE : x) + (ZONE - total_px) / 2 + QUIET * block;
    uint16_t offset_y = (y < 0 ? _h - ZONE : y) + (ZONE - total_px) / 2 + QUIET * block;

    // ── Draw ──────────────────────────────────────────────────────────
    fillRect((x < 0 ? _w - ZONE : x), (y < 0 ? _h - ZONE : y), ZONE, ZONE, WHITE);
    for (uint8_t row = 0; row < qr.size; row++)
        for (uint8_t col = 0; col < qr.size; col++)
            if (qrcode_getModule(&qr, col, row))
                fillRect(offset_x + col * block, offset_y + row * block, block, block, BLACK);

    free(buf);
}