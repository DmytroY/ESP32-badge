void ST7789_76x284::drawChar(int16_t x, int16_t y, char c, uint16_t fg, uint16_t bg, uint8_t scale) {
    if(scale > 6 || scale == 5) scale = 6;
    if (c < ' ' || c > 0x87) return;
    uint8_t idx = c - ' ';

    // 15x21 , scales 3, 6
    if (scale % 3 == 0) {
        const uint16_t* g = font_15x21[idx];
        uint8_t mult = scale / 3;

        for (uint8_t row = 0; row < 21; row++) {
            uint16_t line = pgm_read_word(&g[row]);
            for (uint8_t col = 0; col < 15; col++) {
                uint16_t color = (line & (1 << col)) ? fg : bg;
                fillRect((x + 15 * mult) - col * mult - 1, y + row * mult, mult, mult, color);
            }
        }
        // Character gap
        fillRect(x + 15 * mult, y, mult, 21 * mult, bg);

    // 10x14 , scales 2, 4
    } else if (scale % 2 == 0) {
        const uint16_t* g = font_10x14[idx];
        uint8_t mult = scale / 2;

        for (uint8_t row = 0; row < 14; row++) {
            uint16_t line = pgm_read_word(&g[row]);
            for (uint8_t col = 0; col < 10; col++) {
                uint16_t color = (line & (1 << col)) ? fg : bg;
                fillRect((x + 10* mult) - col * mult - 1, y + row * mult, mult, mult, color);
            }
        }
        // Character gap
        fillRect(x + 10 * mult, y, mult, 14 * mult, bg);

    // 5x7, scale 1
    } else {
        const uint8_t* g = font_5x7[idx];
        for (uint8_t row = 0; row < 7; row++) {
            uint8_t h_line = pgm_read_byte(&g[row]);
            for (uint8_t col = 0; col < 5; col++) {
                uint16_t color = (h_line & (1 << col)) ? fg : bg;
                fillRect((x + 5 * scale) - col * scale - 1, y + row * scale, scale, scale, color);
            }
        }
        // Character gap (Vertical bar to the right)
        fillRect(x + 5 * scale, y, scale, 7 * scale, bg);
    }
}

void ST7789_76x284::drawText(int16_t x, int16_t y, const char* str, uint16_t fg, uint16_t bg, uint8_t scale){
    int16_t cx = x;
    while (*str) {
        if (*str == '\n') {
            cx = x;
            y += 8 * scale;
        } else {
            drawChar(cx, y, *str, fg, bg, scale);
            cx += 6 * scale;
        }
        str++;
    }
}

void ST7789_76x284::drawQR(const char* str, int16_t x, int16_t y) {

    const uint8_t QR_VERSION  = 3;
    const uint8_t QUIET       = 2;
    const uint16_t ZONE       = 76;

    // ── Generate QR ──────────────────────────────────────────────────
    uint8_t* buf = (uint8_t*)malloc(qrcode_getBufferSize(QR_VERSION));
    if (!buf) return;
    QRCode qr;
    qrcode_initText(&qr, buf, QR_VERSION, ECC_LOW, str);

    // ── Block size and centering ──────────────────────────────────────
    uint8_t  block    = ZONE / (qr.size + 2 * QUIET);
    uint16_t total_px = (qr.size + 2 * QUIET) * block;
    uint16_t offset_x = (x < 0 ? _w - ZONE : x) + (ZONE - total_px) / 2 + QUIET * block;
    uint16_t offset_y = (y < 0 ? _h - ZONE : y) + (ZONE - total_px) / 2 + QUIET * block;

    // ── Draw ──────────────────────────────────────────────────────────
    fillRect((x < 0 ? _w - ZONE : x), (y < 0 ? _h - ZONE : y), ZONE, ZONE, WHITE);
    for (uint8_t row = 0; row < qr.size; row++)
        for (uint8_t col = 0; col < qr.size; col++)
            if (qrcode_getModule(&qr, col, row))
                fillRect(offset_x + col * block, offset_y + row * block, block, block, BLACK);

    free(buf);
}

void ST7789_76x284::initPWM(int8_t pin) {
    _bl = pin;
    if (_bl < 0) return;

    // 1. Setup the Timer
    ledc_timer_config_t ledc_timer = {
        .speed_mode       = LEDC_LOW_SPEED_MODE, // C3 only supports Low Speed
        .duty_resolution  = LEDC_TIMER_8_BIT,    // 0-255 range
        .timer_num        = LEDC_TIMER_0,
        .freq_hz          = 5000,                // 5kHz frequency
        // .clk_cfg          = LEDC_AUTO_CLK
        .clk_cfg = LEDC_USE_RTC8M_CLK
    };
    ledc_timer_config(&ledc_timer);

    // 2. Setup the Channel
    ledc_channel_config_t ledc_channel = {
        .gpio_num       = (gpio_num_t)_bl,
        .speed_mode     = LEDC_LOW_SPEED_MODE,
        .channel        = LEDC_CHANNEL_0,
        .intr_type      = LEDC_INTR_DISABLE,
        .timer_sel      = LEDC_TIMER_0,
        .duty           = 127,                     // Start at 50% brightness
        .hpoint         = 0
    };
    ledc_channel_config(&ledc_channel);
}

void ST7789_76x284::setBrightness(uint8_t percentage) {
    if (_bl < 0) return;
    if (percentage > 100) percentage = 100;

    // Calculate duty (0-255)
    uint32_t duty = (percentage * 255) / 100;

    // Invert for Active-Low hardware:
    // 100% -> 0 duty
    // 0%   -> 255 duty
    uint32_t inverted_duty = 255 - duty;

    ledc_set_duty(LEDC_LOW_SPEED_MODE, LEDC_CHANNEL_0, inverted_duty);
    ledc_update_duty(LEDC_LOW_SPEED_MODE, LEDC_CHANNEL_0);
}